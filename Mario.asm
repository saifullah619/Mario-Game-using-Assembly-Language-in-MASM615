collisiondetection Proc
	;player1.parameters.ycoord, player1.parameters.xcoord, player1.parameters.leny, player1.parameters.lenx, player1.pikachu1
	push ax
	.if colisionside == 'R'
		
		mov ax, player1.parameters.xcoord
		add ax, player1.parameters.lenx
		sub ax, 4
		mov tmp, ax
		collision   tmp, player1.parameters.ycoord,log1.parameters.xcoord, log1.parameters.ycoord, colide
		
		.if colide == 0
			add ax, 1
			mov tmp, ax
			collision  tmp, player1.parameters.ycoord, log2.parameters.xcoord, log2.parameters.ycoord, colide
		.endif
		
		.if colide == 0
			add ax, 1
			mov tmp, ax
			collision  tmp, player1.parameters.ycoord, log3.parameters.xcoord, log3.parameters.ycoord, colide
		.endif
		.if colide == 0
		
			collision tmp, player1.parameters.ycoord, 320, player1.parameters.ycoord, colide
			.if colide ==1
				mov levelincrementor,1
				mov colide,0
			.endif
		.endif
		
	.elseif colisionside == 'L'
			
		mov ax, log1.parameters.xcoord
		add ax, log1.parameters.lenx
		add ax, 3
		mov tmp, ax
		collision  player1.parameters.xcoord, player1.parameters.ycoord, tmp ,log1.parameters.ycoord,colide
		
		.if colide == 0
			mov ax, log2.parameters.xcoord
			add ax, log2.parameters.lenx
			sub ax, 4
			mov tmp, ax
			collision   player1.parameters.xcoord, player1.parameters.ycoord,tmp, log2.parameters.ycoord, colide
		.endif
		
		.if colide == 0
			mov ax, log3.parameters.xcoord
			add ax, log3.parameters.lenx
			sub ax, 4
			mov tmp, ax
			collision   player1.parameters.xcoord, player1.parameters.ycoord,tmp, log3.parameters.ycoord, colide
		.endif
		
		.if colide == 0
			collision player1.parameters.xcoord, player1.parameters.ycoord, 00, player1.parameters.ycoord, colide
		.endif
	.endif
	
	pop ax
	ret
collisiondetection	endp
	
