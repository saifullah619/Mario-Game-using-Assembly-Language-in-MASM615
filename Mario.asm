	mov ax,player1.isJumping
	.if ax!=player1.jumpheight
		
		.if player1.jd==0
			call clearpikachu
			push ax
				mov ax,player1.jumpspeed
				.if ax <= decceleration
					mov ax,3
				.else
					sub ax,decceleration
					mov player1.jumpspeed,ax
				.endif
			sub player1.parameters.ycoord,ax
			pop ax
			inc player1.isJumping
			call drawpikachu

		.else
			call clearpikachu
			push ax
			mov ax,player1.jumpspeed
			add ax,acceleration
			mov player1.jumpspeed,ax
			add player1.parameters.ycoord,ax
			pop ax
			
			
			mov ax, player1.parameters.xcoord
            add ax, player1.parameters.lenx
            .if ax  > log1.parameters.xcoord
                mov ax, log1.parameters.xcoord
                add ax, log1.parameters.lenx
                .if ax > player1.parameters.xcoord
                    mov ax,player1.parameters.ycoord
                    add ax,player1.parameters.leny
                    .if ax >= log1.parameters.ycoord
                        mov player1.jd,0
                        mov ax,log1.parameters.ycoord
                        sub ax,player1.parameters.leny
                        mov player1.parameters.ycoord,ax
                        mov player1.isJumping,0
                    .endif
                .endif
            .endif
			
			mov ax, player1.parameters.xcoord
            add ax, player1.parameters.lenx
            .if ax  > log2.parameters.xcoord
                mov ax, log2.parameters.xcoord
                add ax, log2.parameters.lenx
                .if ax > player1.parameters.xcoord
                    mov ax,player1.parameters.ycoord
                    add ax,player1.parameters.leny
                    .if ax >= log2.parameters.ycoord
                        mov player1.jd,0
                        mov ax,log2.parameters.ycoord
                        sub ax,player1.parameters.leny
                        mov player1.parameters.ycoord,ax
                        mov player1.isJumping,0
                    .endif
                .endif
            .endif
			
						mov ax, player1.parameters.xcoord
            add ax, player1.parameters.lenx
            .if ax  > log3.parameters.xcoord
                mov ax, log3.parameters.xcoord
                add ax, log3.parameters.lenx
                .if ax > player1.parameters.xcoord
                    mov ax,player1.parameters.ycoord
                    add ax,player1.parameters.leny
                    .if ax >= log3.parameters.ycoord
                        mov player1.jd,0
                        mov ax,log3.parameters.ycoord
                        sub ax,player1.parameters.leny
                        mov player1.parameters.ycoord,ax
                        mov player1.isJumping,0
                    .endif
                .endif
            .endif
			
			
			
			.if player1.parameters.ycoord>=162
				mov player1.isJumping,0
				mov player1.parameters.ycoord,162
				mov player1.jd,0
			.endif
			call drawpikachu
		.endif
	.else
		mov player1.jd,1
		inc player1.isJumping
		mov player1.jumpspeed,0
	.endif

pop ax

	ret
playerJump endp

